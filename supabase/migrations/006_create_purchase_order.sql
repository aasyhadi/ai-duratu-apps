-- ============================================================
-- MIGRATION: Purchase Order
-- Duratu Kafe ERP
--
-- Membuat:
-- 1. Sequence nomor Purchase Order.
-- 2. Tabel purchase_orders.
-- 3. Tabel purchase_order_items.
-- 4. Constraint dan index.
-- 5. RPC create_purchase_order_from_request.
--
-- Alur:
-- Purchase Request approved
--          ↓
-- Purchase Order draft
--          ↓
-- Purchase Request converted
--
-- Pembuatan PO belum menambah stok.
-- Stok hanya berubah ketika Goods Receipt diproses.
-- ============================================================


-- ============================================================
-- 1. PASTIKAN STATUS PURCHASE REQUEST MENDUKUNG "converted"
-- ============================================================

do $$
declare
  v_constraint record;
begin
  for v_constraint in
    select
      con.conname
    from pg_constraint as con
    join pg_class as cls
      on cls.oid = con.conrelid
    join pg_namespace as ns
      on ns.oid = cls.relnamespace
    join pg_attribute as attr
      on
        attr.attrelid = cls.oid
        and attr.attnum = any(con.conkey)
    where
      ns.nspname = 'public'
      and cls.relname = 'purchase_requests'
      and con.contype = 'c'
      and attr.attname = 'status'
  loop
    execute format(
      'alter table public.purchase_requests drop constraint if exists %I',
      v_constraint.conname
    );
  end loop;
end;
$$;

alter table public.purchase_requests
add constraint purchase_requests_status_valid
check (
  status in (
    'draft',
    'submitted',
    'approved',
    'rejected',
    'converted',
    'cancelled'
  )
);


-- ============================================================
-- 2. SEQUENCE NOMOR PURCHASE ORDER
-- ============================================================

create sequence if not exists
public.purchase_order_number_seq
as bigint
start with 1
increment by 1
minvalue 1
no maxvalue
cache 1;


-- ============================================================
-- 3. TABEL PURCHASE ORDERS
-- ============================================================

create table if not exists public.purchase_orders (
  id bigint generated by default as identity primary key,

  order_number text not null,

  purchase_request_id bigint,
  supplier_id bigint not null,

  status text not null default 'draft',

  order_date date not null default current_date,
  expected_date date,

  notes text,

  subtotal numeric(18, 2) not null default 0,
  discount_amount numeric(18, 2) not null default 0,
  tax_amount numeric(18, 2) not null default 0,
  total_amount numeric(18, 2) not null default 0,

  sent_at timestamptz,
  confirmed_at timestamptz,
  completed_at timestamptz,
  cancelled_at timestamptz,

  cancellation_reason text,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint purchase_orders_order_number_unique
    unique (order_number),

  constraint purchase_orders_purchase_request_unique
    unique (purchase_request_id),

  constraint purchase_orders_purchase_request_id_fkey
    foreign key (purchase_request_id)
    references public.purchase_requests(id)
    on update cascade
    on delete restrict,

  constraint purchase_orders_supplier_id_fkey
    foreign key (supplier_id)
    references public.suppliers(id)
    on update cascade
    on delete restrict,

  constraint purchase_orders_status_valid
    check (
      status in (
        'draft',
        'sent',
        'confirmed',
        'partial_received',
        'completed',
        'cancelled'
      )
    ),

  constraint purchase_orders_subtotal_non_negative
    check (subtotal >= 0),

  constraint purchase_orders_discount_amount_non_negative
    check (discount_amount >= 0),

  constraint purchase_orders_tax_amount_non_negative
    check (tax_amount >= 0),

  constraint purchase_orders_total_amount_non_negative
    check (total_amount >= 0),

  constraint purchase_orders_expected_date_valid
    check (
      expected_date is null
      or expected_date >= order_date
    ),

  constraint purchase_orders_cancellation_reason_valid
    check (
      cancellation_reason is null
      or char_length(btrim(cancellation_reason)) between 5 and 500
    )
);


-- ============================================================
-- 4. TABEL PURCHASE ORDER ITEMS
-- ============================================================

create table if not exists public.purchase_order_items (
  id bigint generated by default as identity primary key,

  purchase_order_id bigint not null,
  product_id bigint not null,

  quantity numeric(18, 3) not null,
  received_quantity numeric(18, 3) not null default 0,

  unit_cost numeric(18, 2) not null,
  subtotal numeric(18, 2) not null,

  notes text,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint purchase_order_items_purchase_order_id_fkey
    foreign key (purchase_order_id)
    references public.purchase_orders(id)
    on update cascade
    on delete cascade,

  constraint purchase_order_items_product_id_fkey
    foreign key (product_id)
    references public.products(id)
    on update cascade
    on delete restrict,

  constraint purchase_order_items_product_unique
    unique (
      purchase_order_id,
      product_id
    ),

  constraint purchase_order_items_quantity_positive
    check (quantity > 0),

  constraint purchase_order_items_received_quantity_non_negative
    check (received_quantity >= 0),

  constraint purchase_order_items_received_quantity_valid
    check (received_quantity <= quantity),

  constraint purchase_order_items_unit_cost_non_negative
    check (unit_cost >= 0),

  constraint purchase_order_items_subtotal_non_negative
    check (subtotal >= 0)
);


