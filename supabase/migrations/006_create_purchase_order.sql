-- ============================================================
-- 6. FUNCTION PEMBUAT NOMOR PURCHASE ORDER
-- ============================================================

create or replace function public.generate_purchase_order_number()
returns text
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_sequence_value bigint;
begin
  v_sequence_value :=
    nextval(
      'public.purchase_order_number_seq'
    );

  return
    'PO-'
    || to_char(
      current_date,
      'YYYYMMDD'
    )
    || '-'
    || lpad(
      v_sequence_value::text,
      6,
      '0'
    );
end;
$$;


-- ============================================================
-- 7. RPC CREATE PURCHASE ORDER FROM PURCHASE REQUEST
-- ============================================================

create or replace function public.create_purchase_order_from_request(
  p_purchase_request_id bigint,
  p_order_date date default current_date,
  p_expected_date date default null,
  p_notes text default null,
  p_discount_amount numeric default 0,
  p_tax_amount numeric default 0
)
returns table (
  purchase_order_id bigint,
  order_number text,
  purchase_request_id bigint,
  supplier_id bigint,
  status text,
  subtotal numeric,
  discount_amount numeric,
  tax_amount numeric,
  total_amount numeric
)
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_request_number text;
  v_request_status text;
  v_supplier_id bigint;

  v_supplier_exists boolean;
  v_existing_order_id bigint;

  v_item_count integer := 0;
  v_invalid_item_count integer := 0;

  v_order_number text;
  v_purchase_order_id bigint;

  v_subtotal numeric(18, 2) := 0;
  v_discount_amount numeric(18, 2) := 0;
  v_tax_amount numeric(18, 2) := 0;
  v_total_amount numeric(18, 2) := 0;

  v_notes text;
begin
  -- ----------------------------------------------------------
  -- Validasi ID Purchase Request
  -- ----------------------------------------------------------
  if
    p_purchase_request_id is null
    or p_purchase_request_id < 1
  then
    raise exception
      using message =
        'ID permintaan pembelian tidak valid.';
  end if;

  -- ----------------------------------------------------------
  -- Validasi tanggal
  -- ----------------------------------------------------------
  if p_order_date is null then
    raise exception
      using message =
        'Tanggal Purchase Order wajib diisi.';
  end if;

  if
    p_expected_date is not null
    and p_expected_date < p_order_date
  then
    raise exception
      using message =
        'Tanggal kebutuhan tidak boleh lebih awal dari tanggal Purchase Order.';
  end if;

  -- ----------------------------------------------------------
  -- Validasi nilai potongan dan pajak
  -- ----------------------------------------------------------
  v_discount_amount :=
    coalesce(
      p_discount_amount,
      0
    );

  v_tax_amount :=
    coalesce(
      p_tax_amount,
      0
    );

  if v_discount_amount < 0 then
    raise exception
      using message =
        'Nilai potongan tidak boleh negatif.';
  end if;

  if v_tax_amount < 0 then
    raise exception
      using message =
        'Nilai pajak tidak boleh negatif.';
  end if;

  v_notes :=
    nullif(
      btrim(
        coalesce(
          p_notes,
          ''
        )
      ),
      ''
    );

  -- ----------------------------------------------------------
  -- Ambil dan kunci Purchase Request
  -- ----------------------------------------------------------
  select
    pr.request_number,
    pr.status,
    pr.supplier_id
  into
    v_request_number,
    v_request_status,
    v_supplier_id
  from public.purchase_requests as pr
  where pr.id = p_purchase_request_id
  for update;

  if not found then
    raise exception
      using message =
        'Permintaan pembelian tidak ditemukan.';
  end if;

  -- ----------------------------------------------------------
  -- Hanya PR approved yang dapat dikonversi
  -- ----------------------------------------------------------
  if v_request_status <> 'approved' then
    raise exception
      using message =
        format(
          'Permintaan pembelian %s tidak dapat dikonversi karena status saat ini adalah %s.',
          v_request_number,
          v_request_status
        );
  end if;

  -- ----------------------------------------------------------
  -- Supplier wajib tersedia
  -- ----------------------------------------------------------
  if v_supplier_id is null then
    raise exception
      using message =
        'Supplier pada permintaan pembelian wajib dipilih sebelum membuat Purchase Order.';
  end if;

  select exists (
    select 1
    from public.suppliers as s
    where
      s.id = v_supplier_id
      and s.is_active is true
  )
  into v_supplier_exists;

  if not v_supplier_exists then
    raise exception
      using message =
        'Supplier tidak ditemukan atau sudah tidak aktif.';
  end if;

  -- ----------------------------------------------------------
  -- Pastikan PR belum pernah dikonversi
  -- ----------------------------------------------------------
  select po.id
  into v_existing_order_id
  from public.purchase_orders as po
  where po.purchase_request_id = p_purchase_request_id
  limit 1;

  if v_existing_order_id is not null then
    raise exception
      using message =
        format(
          'Permintaan pembelian %s sudah pernah dikonversi menjadi Purchase Order.',
          v_request_number
        );
  end if;

  -- ----------------------------------------------------------
  -- Pastikan PR memiliki item
  -- ----------------------------------------------------------
  select count(*)
  into v_item_count
  from public.purchase_request_items as pri
  where pri.purchase_request_id = p_purchase_request_id;

  if v_item_count < 1 then
    raise exception
      using message =
        'Permintaan pembelian harus memiliki minimal satu item.';
  end if;

  -- ----------------------------------------------------------
  -- Validasi item dan produk
  -- ----------------------------------------------------------
  select count(*)
  into v_invalid_item_count
  from public.purchase_request_items as pri
  left join public.products as p
    on p.id = pri.product_id
  where
    pri.purchase_request_id = p_purchase_request_id
    and (
      p.id is null
      or p.is_active is not true
      or p.track_stock is not true
      or pri.quantity is null
      or pri.quantity <= 0
      or pri.unit_cost is null
      or pri.unit_cost < 0
    );

  if v_invalid_item_count > 0 then
    raise exception
      using message =
        'Terdapat produk atau item yang tidak valid pada permintaan pembelian.';
  end if;

  -- ----------------------------------------------------------
  -- Hitung subtotal berdasarkan sumber item
  -- ----------------------------------------------------------
  select
    coalesce(
      sum(
        pri.quantity
        * pri.unit_cost
      ),
      0
    )
  into v_subtotal
  from public.purchase_request_items as pri
  where pri.purchase_request_id = p_purchase_request_id;

  if v_discount_amount > v_subtotal then
    raise exception
      using message =
        'Nilai potongan tidak boleh lebih besar dari subtotal.';
  end if;

  v_total_amount :=
    v_subtotal
    - v_discount_amount
    + v_tax_amount;

  if v_total_amount < 0 then
    raise exception
      using message =
        'Total Purchase Order tidak boleh negatif.';
  end if;

  -- ----------------------------------------------------------
  -- Buat nomor Purchase Order
  -- ----------------------------------------------------------
  v_order_number :=
    public.generate_purchase_order_number();

  -- ----------------------------------------------------------
  -- Buat header Purchase Order
  -- ----------------------------------------------------------
  insert into public.purchase_orders (
    order_number,
    purchase_request_id,
    supplier_id,
    status,
    order_date,
    expected_date,
    notes,
    subtotal,
    discount_amount,
    tax_amount,
    total_amount
  )
  values (
    v_order_number,
    p_purchase_request_id,
    v_supplier_id,
    'draft',
    p_order_date,
    p_expected_date,
    v_notes,
    v_subtotal,
    v_discount_amount,
    v_tax_amount,
    v_total_amount
  )
  returning id
  into v_purchase_order_id;

  -- ----------------------------------------------------------
  -- Salin item Purchase Request ke Purchase Order
  -- ----------------------------------------------------------
  insert into public.purchase_order_items (
    purchase_order_id,
    product_id,
    quantity,
    received_quantity,
    unit_cost,
    subtotal,
    notes
  )
  select
    v_purchase_order_id,
    pri.product_id,
    pri.quantity,
    0,
    pri.unit_cost,
    pri.quantity * pri.unit_cost,
    pri.notes
  from public.purchase_request_items as pri
  where pri.purchase_request_id = p_purchase_request_id
  order by pri.id;

  -- ----------------------------------------------------------
  -- Ubah status Purchase Request menjadi converted
  -- ----------------------------------------------------------
  update public.purchase_requests
  set
    status = 'converted',
    updated_at = now()
  where id = p_purchase_request_id;

  -- ----------------------------------------------------------
  -- Return hasil
  -- ----------------------------------------------------------
  return query
  select
    v_purchase_order_id,
    v_order_number,
    p_purchase_request_id,
    v_supplier_id,
    'draft'::text,
    v_subtotal,
    v_discount_amount,
    v_tax_amount,
    v_total_amount;
end;
$$;


